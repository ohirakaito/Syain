package app;

public class Keihi {
	private String keihiNo;
	private String keihiYmd;
	private String name;
	private String title;
	private String pay;
	private String money;
	private String updateYmd;
	private String updateName;
	private String states;
	private String reason;


	public String getKeihiNo() {
		return keihiNo;
	}
	public void setKeihiNo(String keihiNo) {
		this.keihiNo = keihiNo;
	}
	public String getKeihiYmd() {
		return keihiYmd;
	}
	public void setKeihiYmd(String keihiYmd) {
		this.keihiYmd = keihiYmd;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getUpdateYmd() {
		return updateYmd;
	}
	public void setUpdateYmd(String updateYmd) {
		this.updateYmd = updateYmd;
	}
	public String getUpdateName() {
		return updateName;
	}
	public void setUpdateName(String updateName) {
		this.updateName = updateName;
	}
	public String getStates() {
		return states;
	}
	public void setStates(String states) {
		this.states = states;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
}
